package application;

public class Variaveis {

	public static void main(String[] args) {

		long l = 1l;
		int i = 1;
		byte b = 8;
		float f = 1f;
		short s = 1;
		
		//atribuições long
		float fl = l;
		double dl = l;
		
		//atribuições int
		long li = i;
		float fi = i;
		double di = i;
		
		//atribuições byte
		short sb = b;
		int ib = b;
		long lb = b;
		float fb = b;
		double db = b;
		
		//atribuições float
		double df = f;
		
		//atribuições short
		int is = s;
		long ls = s;
		float fs = s;
		double ds = s;
		
	}

}
